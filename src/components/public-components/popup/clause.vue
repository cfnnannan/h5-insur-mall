                    <br>(1)保险合同或电子保险单号；
                    <br>(2)申请人的有效身份证件；
                    <br>(3)卫生行政部门认定的二级以上（含二级）公立医院出具的附有病理显微镜检查报告、血液检查及其他科学方法检验报告的病史资料及疾病诊断报告书；但原发性心肌病的理赔须由三级以上（含三级）医院出具前述报告和资料；如有必要，我们有权对被保险人进行复检，复检费用由我们承担；
                    <br>(4)所能提供的与确认保险事故的性质、原因、伤害程度等有关的其他证明和资料。
                    以上保险金申请的证明和资料不完整的，我们将及时一次性通知申请人补充提供有关证明和资料。
                    <br>
                    3.4保险金给付<br>
                    我们在收到保险金给付申请书及合同约定的证明和资料后，将在5个工作日内作出核定；情形复杂的，在30日内作出核定。对属于保险责任的，我们在与受益人达成给付保险金的协议后10日内，履行给付保险金义务。
                    我们未及时履行前款规定义务的，除支付保险金外，应当赔偿受益人因此受到的损失。前述“损失”是指根据中国人民银行公布的同时期的人民币活期存款基准利率计算的利息损失。
                    对不属于保险责任的，我们自作出核定之日起3日内向受益人发出拒绝给付保险金通知书并说明理由。
                    我们在收到保险金给付申请书及有关证明和资料之日起60日内，对给付保险金的数额不能确定的，根据已有证明和资料可以确定的数额先予支付；我们最终确定给付保险金的数额后，将支付相应的差额。
                    <br>
                    3.5诉讼时效<br>
                    受益人向我们请求给付保险金的诉讼时效期间为2年，自其知道或者应当知道保险事故发生之日起计算。
                    <br>
                    4.【重大疾病的定义】<br>
                    4.1重大疾病的定义<br>
                    本合同所保障的重大疾病，是指被保险人因遭受意外伤害或自本合同生效之日起60日（60日为疾病观察期，续保无疾病观察期）后因意外伤害以外的原因，被专科医生明确诊断初次患下列疾病或初次达到下列疾病状态或在医院初次接受下列手术：
                    <br>
                    4.1.1恶性肿瘤<br>
                    指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，可以经血管、淋巴管和体腔扩散转移到身体其他部位的疾病。经病理学检查结果明确诊断，临床诊断属于世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD-10）的恶性肿瘤范畴。 下列疾病不在保障范围内： (1)原位癌； (2)相当于Ann Arbor分期方案I期程度的何杰金氏病； (3)皮肤癌（不包括恶性黑色素瘤及已发生转移的皮肤癌）； (4)TNM分期为T1N0M0期或更轻分期的前列腺癌； (5)感染艾滋病病毒或患艾滋病期间所患恶性肿瘤。
                    <br>
                    4.1.2重大器官移植<br>
                    重大器官移植术，指因相应器官功能衰竭，已经实施了肾脏、肝脏、心脏或
                    术或造血干细胞移植术
                    肺脏的异体移植手术。
                    造血干细胞移植术，指因造血功能损害或造血系统恶性肿瘤，已经实施了造血干细胞（包括骨髓造血干细胞、外周血造血干细胞和脐血造血干细胞）的异体移植手术。
                    <br>
                    4.1.3终末期肾病（或称慢性肾功能衰竭尿毒症期）<br>
                    指双肾功能慢性不可逆性衰竭，达到尿毒症期，经诊断后已经进行了至少90天的规律性透析治疗或实施了肾脏移植手术。
                    <br>
                    4.1.4多个肢体缺失<br>
                    指因疾病或意外伤害导致两个或两个以上肢体自腕关节或踝关节近端（靠近躯干端）以上完全性断离。
                    <br>
                    4.1.5急性或亚急性重症肝炎<br>
                    指因肝炎病毒感染引起肝脏组织弥漫性坏死，导致急性肝功能衰竭，且经血清学或病毒学检查证实，并须满足下列全部条件：
                    <br>(1)重度黄疸或黄疸迅速加重；
                    <br>(2)肝性脑病；
                    <br>(3)B超或其他影像学检查显示肝脏体积急速萎缩；
                    <br>(4)肝功能指标进行性恶化。
                    <br>
                    4.1.6良性脑肿瘤<br>
                    指脑的良性肿瘤，已经引起颅内压增高，临床表现为视神经乳头水肿、精神症状、癫痫及运动感觉障碍等，并危及生命。须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实，并须满足下列至少一项条件：
                    <br>(1)实际实施了开颅进行的脑肿瘤完全切除或部分切除的手术；
                    <br>(2)实际实施了对脑肿瘤进行的放射治疗。 脑垂体瘤、脑囊肿、脑血管性疾病不在保障范围内。
                    <br>
                    4.1.7脑炎后遗症或脑膜炎后遗症<br>
                    指因患脑炎或脑膜炎导致的神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病确诊180天后，仍遗留下列一种或一种以上障碍：
                    <br>(1)一肢或一肢以上肢体机能完全丧失；
                    <br>(2)语言能力或咀嚼吞咽能力完全丧失；
                    <br>(3)自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。
                    <br>>
                    4.1.8深度昏迷<br>
                    指因疾病或意外伤害导致意识丧失,对外界刺激和体内需求均无反应,昏迷程度按照格拉斯哥昏迷分级（Glasgow coma scale）结果为5分或5分以下，且已经持续使用呼吸机及其他生命维持系统96小时以上。 因酗酒或药物滥用导致的深度昏迷不在保障范围内。
                    <br>
                    4.1.9双耳失聪<br>
                    指因疾病或意外伤害导致双耳听力永久不可逆性丧失，在500赫兹、1000赫兹和2000赫兹语音频率下，平均听阈大于90分贝，且经纯音听力测试、声导抗检测或听觉诱发电位检测等证实。 申请理赔时，被保险人年龄必须在3周岁以上，并且须提供理赔当时的听力丧失诊断及检查证据。
                    <br>
                    4.1.10双目失明<br>
                    指因疾病或意外伤害导致双眼视力永久不可逆性丧失，双眼中较好眼须满足下列至少一项条件：
                    <br>(1)眼球缺失或摘除；
                    <br>(2)矫正视力低于0.02（采用国际标准视力表，如果使用其他视力表应进行换算）；
                    <br>(3)视野半径小于5度。 申请理赔时，被保险人年龄必须在3周岁以上，并且须提供理赔当时的视力丧失诊断及检查证据。
                    <br>
                    4.1.11瘫痪<br>
                    指因疾病或意外伤害导致两肢或两肢以上肢体机能永久完全丧失。肢体机能永久完全丧失，指疾病确诊180天后或意外伤害发生180天后，每肢三大关节中的两大关节仍然完全僵硬，或不能随意识活动。
                    <br>
                    4.1.12心脏瓣膜手术<br>
                    指为治疗心脏瓣膜疾病，实际实施了开胸进行的心脏瓣膜置换或修复的手术。
                    <br>
                    4.1.13严重脑损伤<br>
                    指因头部遭受机械性外力，引起脑重要部位损伤，导致神经系统永久性的功能障碍。须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证实。神经系统永久性的功能障碍，指脑损伤180天后，仍遗留下列一种或一种以上障碍：
                    <br>(1)一肢或一肢以上肢体机能完全丧失；
                    <br>(2)语言能力或咀嚼吞咽能力完全丧失；
                    <br>(3)自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。
                    <br>
                    4.1.14严重Ⅲ度烧伤<br>
                    指烧伤程度为Ⅲ度，且Ⅲ度烧伤的面积达到全身体表面积的20％或20％以上。体表面积根据《中国新九分法》计算。
                    <br>
                    4.1.15严重运动神经元病<br>
                    是一组中枢神经系统运动神经元的进行性变性疾病，包括进行性脊肌萎缩症、进行性延髓麻痹症、原发性侧索硬化症、肌萎缩性侧索硬化症。须满足自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上的条件。
                    <br>
                    4.1.16语言能力丧失<br>
                    指因疾病或意外伤害导致完全丧失语言能力，经过积极治疗至少12个月（声带完全切除不受此时间限制），仍无法通过现有医疗手段恢复。 精神心理因素所致的语言能力丧失不在保障范围内。 申请理赔时，被保险人年龄必须在3周岁以上，并且须提供理赔当时的语言能力丧失诊断及检查证据。
                    <br>
                    4.1.17重型再生障碍性贫血<br>
                    指因骨髓造血功能慢性持续性衰竭导致的贫血、中性粒细胞减少及血小板减少。须满足下列全部条件：
                    <br>(1)骨髓穿刺检查或骨髓活检结果支持诊断；
                    <br>(2)外周血象须具备以下三项条件：
                    <br>①中性粒细胞绝对值≤0.5×109/L ；
                    <br>②网织红细胞＜1％；
                    <br>③血小板绝对值≤20×109/L。
                    <br>
                    4.1.18主动脉手术<br>
                    指为治疗主动脉疾病，实际实施了开胸或开腹进行的切除、置换、修补病损主动脉血管的手术。主动脉指胸主动脉和腹主动脉，不包括胸主动脉和腹主动脉的分支血管。
                    动脉内血管成形术不在保障范围内。
                    <br>
                    4.1.19原发性心肌病<br>
                    指因原发性心肌病导致慢性心功能损害造成永久不可逆性的心功能衰竭。心功能衰竭程度达到纽约心脏病学会心功能分级状态分级IV级。被保险人永久不可逆性的体力活动能力受限，不能从事任何体力活动。
                    <br>
                    4.1.20肌营养不良症<br>
                    由专科医师确认的诊断为Duchenne，Becker，或Limb Girdle 肌营养不良症（所有其他类型的肌营养不良症均不在保障范围内），已进行了至少90天的治疗，并提供肌肉活检和血CPK检测证实。
                    该疾病须导致被保险人自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上，或者导致被保险人永久性的卧床，没有外部帮助无法起床。
                    <br>
                    4.1.21脊髓灰质炎<br>
                    指由于脊髓灰质炎病毒感染所致的瘫痪性疾病，临床表现为运动功能损害或呼吸无力。脊髓灰质炎必须明确诊断。本合同仅对脊髓灰质炎造成的神经系统功能损害导致被保险人一肢或一肢以上肢体机能永久完全丧失的情况予以理赔。肢体机能永久完全丧失，指疾病确诊180天后，每肢三大关节中的两大关节仍然完全僵硬，或不能随意识活动。
                    <br>
                    4.1.22严重心肌炎<br>
                    指心肌局限性或弥漫性的急性或慢性炎症病变，导致心脏功能障碍，达到纽约心脏病学会心功能分级状态分级IV级，且持续至少90天。
                    <br>
                    4.1.23持续植物人状态<br>
                    指由脑外伤、缺氧持续状态、严重的脑炎或者某种神经毒素造成大脑皮层广泛性损害，而脑干功能相对保留所致的一种丧失生理、意识和交流功能后的临床状态，并且该状态已持续30天以上。患者无法从事主动或者有目的的活动，但保留了躯体生存的基本功能，如新陈代谢、生长发育等。
                    <br>
                    4.1.24严重幼年性类风湿关节炎（或称斯蒂尔病）<br>
                    指一种少儿的结缔组织病，以慢性关节炎为其主要特点，并伴有全身多个系统的受累，包括关节、肌肉、肝、脾、淋巴结等。斯蒂尔病须满足下列全部条件：
                    <br>(1)因该病引致广泛性关节破坏，以致需要进行髋及膝关节置换；
                    <br>(2)由风湿病专科医生确定诊断。
                    <br>
                    4.1.25川崎病<br>
                    指一种原因未明的血管炎综合征，临床表现为急性发热，皮肤粘膜病损和淋巴结肿大。本病须经专科医生明确诊断，同时须由血管造影或超声心动图检查证实，满足下列至少一项条件：
                    <br>(1)伴有冠状动脉瘤，且冠状动脉瘤于最初急性发病后持续至少180天；
                    <br>(2)伴有冠状动脉瘤，且实际接受了对冠状动脉瘤进行的手术治疗。
                    <br>
                    4.1.26全身性重症肌无力<br>
                    重症肌无力是一种神经肌肉接头传递障碍所致的疾病，表现为局部或全身骨骼肌（特别是眼外肌）极易疲劳。疾病可以累及呼吸肌、上肢或下肢的近端肌群或全身肌肉，必须满足下列全部条件：
                    <br>(1)经药物治疗和胸腺切除治疗一年以上仍无法控制病情；
                    <br>(2)自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。
                    <br>
                    4.1.27严重克隆病<br>
                    克隆病是一种慢性肉芽肿性肠炎，具有特征性的克隆病病理组织学变化。诊断必须由病理检查结果证实。被保险人所患的克隆病必须已经造成瘘管形成
                    并伴有肠梗阻或肠穿孔。
                    <br>
                    4.1.28肝豆状核变性（或称Wilson病）<br>
                    指一种常染色体隐性遗传的铜代谢缺陷病，以不同程度的肝细胞损害、脑退行性病变和角膜边缘有铜盐沉着环为其临床特征，并须满足下列全部条件：
                    <br>(1)典型症状；
                    <br>(2)角膜色素环（K-F环）；
                    <br>(3)血清铜和血清铜蓝蛋白降低，尿铜增加；
                    <br>(4)经皮做肝脏活检来定量分析肝脏铜的含量。
                    <br>
                    4.1.29严重哮喘<br>
                    被保险人必须在过去两年内曾发生哮喘持续状态，并满足以下标准中的两项或两项以上标准：
                    <br>(1)运动耐受力永久并持续地减少并且轻微的运动能引起气促；
                    <br>(2)长期胸腔过渡膨胀而导致胸腔畸形；
                    <br>(3)在家及在静息状态下需要吸氧；
                    <br>(4)持续的每天服用类固醇药物(至少持续6个月以上)。
                    <br>
                    4.1.30重症手足口病<br>
                    是由肠道病毒引起的急性传染病，主要症状表现为手、足、口腔等部位的斑丘疹、疱疹。重症手足口病必须符合下列全部条件：
                    <br>(1)经专科医生诊断为手足口病；
                    <br>(2)伴有所列危重并发症之一：脑膜炎、脑炎、脑脊髓炎、肺水肿或心肌炎；
                    <br>(3)接受了住院治疗。
                    <br>
                    4.2定义来源及确诊医院范围<br>
                    以上4.1.1至4.1.18所列重大疾病定义根据中国保险行业协会2007年公布的《重大疾病保险的疾病定义使用规范》作出，第4.1.19至4.1.30所列重大疾病由我们自行增加，其定义由我们根据通行的医学标准制定。 以上重大疾病，除原发性心肌病须在卫生行政部门认定的三级以上（含三级）医院确诊外，其他重大疾病均须在卫生行政部门认定的二级以上（含二级）公立医院确诊。
                    <br>
                    5.【特定重大疾病的定义】<br>
                    5.1特定重大疾病的定义<br>
                    本合同所保障的特定重大疾病，是指被保险人因遭受意外伤害或自本合同生效之日起60日（60日为疾病观察期，续保无疾病观察期）后因意外伤害以外的原因，被专科医生明确诊断初次患下列疾病或初次达到下列疾病状态：
                    <br>
                    5.1.1白血病<br>
                    是一种造血系统的恶性肿瘤，其主要表现为白血病细胞在骨髓或其他造血组织中大量克隆、异常增生，大量聚集的白细胞抑制正常造血并浸润全身器官和组织。周围白细胞有质和量的变化，出现相应临床表现。
                    被保险人所患白血病必须根据骨髓的活组织检查和周围血象由专科（儿科、血液科或肿瘤科）医生确诊。
                    <br>
                    5.1.2重症手足口病<br>
                    是由肠道病毒引起的急性传染病，主要症状表现为手、足、口腔等部位的斑丘疹、疱疹。重症手足口病必须符合下列全部条件：
                    <br>(1)经专科医生诊断为手足口病；
                    <br>(2)伴有所列危重并发症之一：脑膜炎、脑炎、脑脊髓炎、肺水肿或心肌炎；
                    <br>(3)接受了住院治疗。
                    <br>
                    5.1.3严重心肌炎<br>
                    指心肌局限性或弥漫性的急性或慢性炎症病变，导致心脏功能障碍，达到纽约心脏病学会心功能分级状态分级IV级，且持续至少90天。
                    <br>
                    5.1.4脑炎后遗症或脑膜炎后遗症<br>
                    指因患脑炎或脑膜炎导致的神经系统永久性的功能障碍。神经系统永久性的功能障碍，指疾病确诊180天后，仍遗留下列一种或一种以上障碍：
                    <br>(1)一肢或一肢以上肢体机能完全丧失；
                    <br>(2)语言能力或咀嚼吞咽能力完全丧失；
                    <br>(3)自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。
                    <br>
                    5.2定义来源及确诊医院范围<br>
                    以上5.1.1至5.1.4所列特定重大疾病定义由我们根据通行的医学标准制定。
                    以上特定重大疾病均须在卫生行政部门认定的二级以上（含二级）公立医院确诊。
                    <br>
                    6.【释义】<br>
                    6.1周岁<br>
                    指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。
                    <br>
                    6.2有效身份证件<br>
                    指由政府主管部门规定的证明其身份的证件，如：居民身份证、按规定可使用的有效护照、军官证、警官证、士兵证、户口簿等证件。
                    <br>
                    6.3现金价值<br>
                    指本合同保险单所具有的价值，通常体现为解除合同时，根据精算原理计算的，由我们退还的那部分金额。现金价值＝本合同的保险费×65％×（1－n/m）其中n为本合同已生效天数，m为本合同保险期间的天数。合同已生效的天数不足一天的不计。
                    <br>
                    6.4意外伤害<br>
                    指遭受外来的、突发的、非本意的、非疾病的客观事件直接致使身体受到的伤害。
                    <br>
                    6.5毒品<br>
                    指中华人民共和国刑法规定的鸦片、海洛因、甲基苯丙胺（冰毒）、吗啡、大麻、可卡因以及国家规定管制的其他能够使人形成瘾癖的麻醉药品和精神药品，但不包括由医生开具并遵医嘱使用的用于治疗疾病但含有毒品成分的处方药品。
                    <br>
                    6.6酒后驾驶<br>
                    指经检测或鉴定，发生事故时车辆驾驶人员每百毫升血液中的酒精含量达到或超过一定的标准，公安机关交通管理部门依据《道路交通安全法》的规定认定为饮酒后驾驶或醉酒后驾驶。
                    <br>
                    6.7无合法有效驾驶证驾驶<br>
                    指下列情形之一：<br>
                    (1)没有取得驾驶资格；<br>
                    (2)驾驶与驾驶证准驾车型不相符合的车辆；<br>
                    (3)持审验不合格的驾驶证驾驶；<br>
                    (4)持学习驾驶证学习驾车时，无教练员随车指导，或不按指定时间、路线学习驾车。<br>
                    <br>
                    6.8无有效行驶证<br>
                    指下列情形之一：<br>
                    (1)机动车被依法注销登记的；<br>
                    (2)未依法按时进行或通过机动车安全技术检验。<br>
                    <br>
                    6.9机动车<br>
                    指以动力装置驱动或者牵引，上道路行驶的供人员乘用或者用于运送物品以及进行工程专项作业的轮式车辆。
                    <br>
                    6.10感染艾滋病病毒或患艾滋病<br>
                    艾滋病病毒指人类免疫缺陷病毒，英文缩写为HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合症，英文缩写为AIDS。
                    在人体血液或其他样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。
                    <br>
                    6.11遗传性疾病<br>
                    指生殖细胞或受精卵的遗传物质（染色体和基因）发生突变或畸变所引起的疾病，通常具有由亲代传至后代的垂直传递的特征。
                    <br>
                    6.12先天性畸形、变形或染色体异常<br>
                    指被保险人出生时就具有的畸形、变形或染色体异常。先天性畸形、变形和染色体异常依照世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD-10）确定。
                    <br>
                    6.13情形复杂<br>
                    指保险事故的性质、原因、伤害或损失程度等在我们收到保险金给付申请书及合同约定的证明和资料后5个工作日内无法确定，需要进一步核实。
                    <br>
                    6.14专科医生<br>
                    专科医生应当同时满足以下四项资格条件：<br>
                    (1)具有有效的中华人民共和国《医师资格证书》；<br>
                    (2)具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；<br>
                    (3)具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；<br>
                    (4)在二级或二级以上公立医院的相应科室从事临床工作三年以上。<br>
                    <br>
                    6.15肢体机能完全丧失<br>
                    指肢体的三大关节中的两大关节僵硬，或不能随意识活动。肢体是指包括肩关节的整个上肢或包括髋关节的整个下肢。
                    <br>
                    6.16语言能力或咀嚼吞咽能力完全丧失<br>
                    语言能力完全丧失，指无法发出四种语音（包括口唇音、齿舌音、口盖音和喉头音）中的任何三种、或声带全部切除，或因大脑语言中枢受伤害而患失语症。
                    咀嚼吞咽能力完全丧失，指因牙齿以外的原因导致器质障碍或机能障碍，以致不能作咀嚼吞咽运动，除流质食物外不能摄取或吞咽的状态。
                    <br>
                    6.17六项基本日常生活活动<br>
                    六项基本日常生活活动是指：<br>
                    (1)穿衣：自己能够穿衣及脱衣；<br>
                    (2)移动：自己从一个房间到另一个房间；<br>
                    (3)行动：自己上下床或上下轮椅；<br>
                    (4)如厕：自己控制进行大小便；<br>
                    (5)进食：自己从已准备好的碗或碟中取食物放入口中；<br>
                    (6)洗澡：自己进行淋浴或盆浴。<br>
                    <br>
                    6.18永久不可逆<br>
                    指自疾病确诊或意外伤害发生之日起，经过积极治疗180天后，仍无法通过现有医疗手段恢复。  

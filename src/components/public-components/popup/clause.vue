                    <br>第三条  本保险合同的投保人应为具有完全民事行为能力的被保险人本人、对被保险人有保险利益的其他人。
                    <br>第四条  本保险合同的受益人包括：
                    <br>（一）身故保险金受益人
                    <br>订立本保险合同时，被保险人或投保人可指定一人或数人为身故保险金受益人。身故保险金受益人为数人时，应确定其受益顺序和受益份额；未确定受益份额的，各身故保险金受益人按照相等份额享有受益权。投保人指定受益人时须经被保险人同意。
                    <br>被保险人死亡后，有下列情形之一的，保险金作为被保险人的遗产，由保险人依照《中华人民共和国继承法》的规定履行给付保险金的义务：
                    <br>1．没有指定受益人，或者受益人指定不明无法确定的；
                    <br>2．受益人先于被保险人死亡，没有其他受益人的；
                    <br>3．受益人依法丧失受益权或者放弃受益权，没有其他受益人的。
                    <br>受益人与被保险人在同一事件中死亡，且不能确定死亡先后顺序的，推定受益人死亡在先。
                    <br>被保险人或投保人可以变更身故保险金受益人，但需书面通知保险人，由保险人在本保险合同上批注。对因身故保险金受益人变更发生的法律纠纷，保险人不承担任何责任。
                    <br>投保人指定或变更身故保险金受益人的，应经被保险人书面同意。被保险人为无民事行为能力人或限制民事行为能力人的，应由其监护人指定或变更身故保险金受益人。
                    <br>（二）伤残保险金受益人
                    <br>除另有约定外，本保险合同的伤残保险金的受益人为被保险人本人。
                    <p class="title-item">保险责任</p>
                    第五条 在保险期间内，被保险人因遭受意外伤害事故导致身故、伤残的，保险人依照下列约定给付保险金。
                    <br>（一）身故保险责任
                    <br>在保险期间内，被保险人遭受意外伤害事故，并自事故发生之日起180日内因该事故身故的，保险人按意外伤害保险金额给付身故保险金，对该被保险人的保险责任终止。
                    <br>被保险人因遭受意外伤害事故且自该事故发生日起下落不明，后经人民法院宣告死亡的，保险人按意外伤害保险金额给付身故保险金。但若被保险人被宣告死亡后生还的，保险金受领人应于知道或应当知道被保险人生还后30日内退还保险人给付的身故保险金。
                    <br>被保险人身故前保险人已给付第（二）款约定的伤残保险金的，身故保险金应扣除已给付的保险金。
                    <br>（二）伤残保险责任
                    <br>在保险期间内，被保险人遭受意外伤害事故，并自该事故发生之日起180日内因该事故造成本保险合同所附《人身保险伤残评定标准与代码》（JR/T 0083—2013）（简称《伤残评定标准》）所列伤残之一的，保险人按该表所列给付比例乘以意外伤害保险金额给付伤残保险金。如第180日治疗仍未结束的，按当日的身体情况进行伤残鉴定，并据此给付伤残保险金。
                    <br>1．当同一保险事故造成两处或两处以上伤残时，应首先对各处伤残程度分别进行评定，如果几处伤残等级不同，以最重的伤残等级作为最终的评定结论；如果两处或两处以上伤残等级相同，伤残等级在原评定基础上最多晋升一级，最高晋升至第一级。同一部位和性质的伤残，不应采用《伤残评定标准》条文两条以上或者同一条文两次以上进行评定。
                    <br>2．被保险人如在本次意外伤害事故之前已有伤残，保险人按合并后的伤残程度在《伤残评定标准》中所对应的给付比例给付伤残保险金，但应扣除原有伤残程度在《伤残评定标准》所对应的伤残保险金。
                    <br>在保险期间内，前述第（一）、（二）款下的保险金累计给付金额以保险单载明的意外伤害保险金额为限。
                    <p class="title-item">责任免除</p>
                    第六条  因下列原因造成被保险人身故、伤残的，保险人不承担给付保险金责任：
                    <br>（一）投保人的故意行为；
                    <br>（二）被保险人自致伤害或自杀，但被保险人自杀时为无民事行为能力人的除外；
                    <br>（三）因被保险人挑衅或故意行为而导致的打斗、被袭击或被谋杀；
                    <br>（四）被保险人妊娠、流产、分娩、疾病、药物过敏、中暑、猝死；
                    <br>（五）被保险人接受整容手术及其他内、外科手术；
                    <br>（六）被保险人未遵医嘱，私自服用、涂用、注射药物；
                    <br>（七）核爆炸、核辐射或核污染；
                    <br>（八）恐怖袭击；
                    <br>（九）被保险人犯罪或拒捕；
                    <br>（十）被保险人从事高风险运动或参加职业或半职业体育运动。
                    <br>第七条  被保险人在下列期间遭受伤害导致身故、伤残的，保险人也不承担给付保险金责任：
                    <br>（一）战争、军事行动、暴动或武装叛乱期间；
                    <br>（二）被保险人醉酒或毒品、管制药物的影响期间；
                    <br>（三）被保险人酒后驾车、无有效驾驶证驾驶或驾驶无有效行驶证的机动车期间。
                    <br>发生上述第六、七条情形，被保险人身故的，保险人对该被保险人保险责任终止，并对投保人按日计算退还未满期净保费。
                    <br>第八条  保险金额是保险人承担给付保险金责任的最高限额。
                    <br>本保险合同的意外伤害保险金额由投保人、保险人双方约定，并在保险单中载明。
                    <br>投保人应该按照合同约定向保险人交纳保险费。
                    <p class="title-item">保险期间</p>
                    第九条  本保险合同保险期间由保险人和投保人协商确定，以保险单载明的起讫时间为准。
                    <p class="title-item">保险人义务</p>
                    第十条  本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。
                    <br>第十一条  保险人按照第二十条的约定，认为被保险人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人补充提供。
                    <br>第十二条  保险人收到被保险人的给付保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，保险人将在确定是否属于保险责任的基本材料收集齐全后，尽快做出核定。
                    <br>保险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成给付保险金的协议后十日内，履行赔偿保险金义务。保险合同对给付保险金的期限有约定的，保险人应当按照约定履行给付保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝给付保险金通知书，并说明理由。
                    <br>第十三条  保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其给付的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。
                    <p class="title-item">投保人、被保险人义务</p>
                    第十四条  除另有约定外，投保人应当在保险合同成立时交清保险费。 
                    <br>第十五条  若投保人是对被保险人有保险利益的其他人，订立保险合同时，投保人应提供被保险人同意投保的书面文件。
                    <br>第十六条  订立保险合同，保险人就被保险人的有关情况提出询问的，投保人应当如实告知。
                    <br>投保人故意或者因重大过失未履行前款规定的义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本保险合同。
                    <br>前款规定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。
                    <br>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。
                    <br>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，但应当退还保险费。
                    <br>保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。
                    <br>第十七条  被保险人变更职业或工种时，投保人或被保险人应在10日内以书面形式通知保险人。
                    <br>被保险人所变更的职业或工种依照保险人职业分类在拒保范围内的，保险人在接到通知后有权解除本保险合同并按照接到通知之日退还原职业或工种所对应的未满期净保费。被保险人所变更的职业或工种依照保险人职业分类危险程度增加但仍可承保或在拒保范围内但保险人认定可以继续承保的，保险人自接到通知之日起，按其差额增收保险费。
                    <br>被保险人所变更的职业或工种依照保险人职业分类危险程度降低的，保险人自接到通知之日起，按其差额退还未满期净保费。
                    <br>被保险人所变更的职业或工种，依照保险人职业分类其危险性增加，且未依本条约定通知保险人而发生保险事故的，保险人按其原交保险费比新职业或工种所对应的保险费率计算并给付保险金。被保险人所变更的职业或工种依照保险人职业分类在拒保范围内，保险人不承担给付保险金的责任。
                    <br>第十八条  投保人住所或通讯地址变更时，应及时以书面形式通知保险人。投保人未通知的，保险人按本保险合同所载的最后住所或通讯地址发送的有关通知，均视为已发送给投保人。
                    <br>第十九条  投保人、被保险人或者保险金受益人知道保险事故发生后，应当在48小时内及时通知保险人。故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
                    <br>上述约定，不包括因不可抗力而导致的迟延。
                    <p class="title-item">保险金申请与给付</p>
                    第二十条  保险金申请人向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法核实该申请的真实性的，保险人对无法核实部分不承担给付保险金的责任。
                    <br>（一）身故保险金申请
                    <br>1．保险金给付申请书；
                    <br>2．保险单原件；
                    <br>3．保险金申请人的身份证明；
                    <br>4．公安部门或医疗机构出具的被保险人死亡证明书；若被保险人为宣告死亡，保险金申请人应提供人民法院出具的宣告死亡证明文件；若为境外出险，需提供事故发生地使领馆出具的的包含死亡原因的书面证明材料；
                    <br>5．被保险人的户籍注销证明；
                    <br>6．保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；
                    <br>7．若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。
                    <br>除提交上述材料外，保险金申请人申请身故保险金时，为确定事故原因，保险人有权要求由司法鉴定机构对事故原因进行鉴定，如进行尸体检验等。
                    <br>（二）伤残保险金申请
                    <br>1．保险金给付申请书；
                    <br>2．保险单原件；
                    <br>3．被保险人身份证明；
                    <br>4．二级以上（含二级）或保险人认可的医疗机构或司法鉴定机构出具的伤残鉴定诊断书；
                    <br>5．保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；
                    <br>6．若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。
                    <br>第二十一条  保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。
                    <p class="title-item">争议处理和法律适用</p>
                    第二十二条 因履行本保险合同发生的争议，由当事人协商解决，协商不成的，由当事人从下列两种合同争议解决方式中选择一种，并在本保险合同中载明： 
                    <br>（一）提交保险单载明的仲裁委员会仲裁； 
                    <br>（二）依法向人民法院起诉。
                    <br>第二十三条  与本保险合同有关的以及履行本保险合同产生的一切争议处理适用中华人民共和国法律（不包括港澳台地区法律）。
                    <p class="title-item">其他事项</p>
                    第二十四条  投保人和保险人可以协商变更合同内容。
                    <br>变更保险合同的，应当由保险人在保险单或者其他保险凭证上批注或附贴批单，或者投保人和保险人订立变更的书面协议。
                    <br>第二十五条  在本保险合同成立后，投保人可以书面形式通知保险人解除合同，但保险人已根据本保险合同约定给付保险金的除外。
                    <br>投保人解除本保险合同时，应提供下列证明文件和资料：
                    <br>（一）保险合同解除申请书；
                    <br>（二）保险单原件；
                    <br>（三）保险费交付凭证；
                    <br>（四）投保人身份证明。
                    <br>投保人要求解除本保险合同，自保险人接到保险合同解除申请书之时起，本保险合同的效力终止。保险人收到上述证明文件和资料之日起30日内退还保险单的未满期净保费。
                    <br>第二十六条  在本保险合同有效期内，被保险人配偶因离异等原因不再符合本条款“配偶”定义或子女不再符合本条款“子女”定义的，投保人或被保险人应以书面形式通知保险人，保险人对该被保险人所负保险责任自其资格丧失次日零时起终止，并扣除手续费后退还该被保险人的未满期净保费。
                    <p class="title-item">释义</p>
                    第二十七条 
                    <br>【周岁】以法定身份证明文件中记载的出生日期为基础计算的实足年龄。
                    <br>【保险人】指与投保人签订本保险合同的易安财产保险股份有限公司。
                    <br>【意外伤害】指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。

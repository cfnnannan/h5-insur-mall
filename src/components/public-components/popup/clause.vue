                    <p>(2)门诊<b class="b-color">恶性肿瘤</b>治疗费用，包括<b class="b-color">化学疗法</b>、<b class="b-color">放射疗法</b>、<b class="b-color">肿瘤免疫疗法</b>、<b class="b-color">肿瘤内分泌疗法</b>、<b class="b-color">肿瘤靶向疗法</b>治疗费用；</p>
                    <p>(3)器官移植后的门诊抗排异治疗费用；</p>
                    <p>(4)门诊手术费。</p>
                    <p>对于因上述治疗发生的合理且必要的医疗费用，我们以一般医疗保险金额为限，按本合同约定的保险金计算方法计算并给付指定门诊医疗保险金。</p>
                    <p>3、住院前后门诊急诊医疗保险金</p>
                    <p>
                        被保险人在指定医疗机构普通病房住院前7日（含出院当日）内和出院后30日（含出院当日）内，与该次住院相同原因而发生的门急诊医疗费用，不包括门诊肾透析、门诊恶性肿瘤治疗、器官移植后的门诊抗排异治疗和门诊手术费用。
                    </p>
                    <p>
                        对于因上述治疗发生的合理且必要的医疗费用，我们以一般医疗保险金额为限，按本合同约定的保险金计算方法计算并给付住院前后门诊急诊医疗保险金。
                    </p>
                    <p>
                        在任何情况下，我们在保险期间内对被保险人一次或多次累计给付的住院医疗保险金、指定门诊医疗保险金、住院前后门诊急诊医疗保险金总和以一般医疗保险金额为限。当一次或累计给付的住院医疗保险金、指定门诊医疗保险金、住院前后门诊急诊医疗保险金总和达到一般医疗保险金额时，我们给付一般医疗保险金的责任即时终止。                        </p>
                    <p>
                        <span>恶性肿瘤医疗保险金</span>
                    </p>
                    <p>
                        在保险期间内，被保险人在疾病观察期后初次被确诊为恶性肿瘤，在指定医疗机构普通病房或普通门诊部接受治疗的，我们首先按照前款约定给付一般医疗保险金，当我们累计给付金额达到一般医疗保险金金额后，我们依照下列约定给付恶性肿瘤医疗保险金：
                    </p>
                    <p>1、恶性肿瘤住院医疗保险金</p>
                    <p>
                        被保险人因确诊恶性肿瘤经指定医疗机构诊断必须住院治疗的，对于在指定医疗机构普通病房住院期间发生的合理且必要的<b class="b-color">恶性肿瘤住院医疗费用</b>，我们以恶性肿瘤医疗保险金额为限，按本合同约定的保险金计算方法计算并给付恶性肿瘤住院医疗保险金。
                    </p>
                    <p>
                        在每一个保单年度内，恶性肿瘤住院的最高给付日数为180日，累计恶性肿瘤住院超过180日发生的医疗费用，不属于保险责任范围。
                    </p>
                    <p>
                        若被保险人在保险期间内开始恶性肿瘤住院治疗，到保险期间届满住院仍未结束的，我们继续承担给付保险金责任至住院结束，但最长不超过保险期间届满之日起第30日。
                    </p>
                    <p>2、恶性肿瘤特殊门诊医疗保险金</p>
                    <p>
                        被保险人因确诊恶性肿瘤在指定医疗机构普通门诊部进行门诊恶性肿瘤治疗发生的合理且必要的医疗费用，包括化学疗法、放射疗法、肿瘤免疫疗法、肿瘤内分泌疗法、肿瘤靶向疗法治疗费用，我们以恶性肿瘤医疗保险金额为限，按本合同约定的保险金计算方法计算并给付恶性肿瘤特殊门诊医疗保险金。
                    </p>
                    <p>3、恶性肿瘤住院前后门诊急诊医疗保险金</p>
                    <p>
                        被保险人在指定医疗机构普通病房住院前7日（含出院当日）内和出院后30日（含出院当日）内，因为恶性肿瘤而发生的门诊急诊医疗费用，不包括门诊肾透析、门诊恶性肿瘤治疗、器官移植后的门诊抗排异治疗和门诊手术费用。
                    </p>
                    <p>
                        对于因上述治疗发生的合理且必要的医疗费用，我们以恶性肿瘤医疗保险金额为限，按本合同约定的保险金计算方法计算并给付恶性肿瘤住院前后门诊急诊医疗保险金。
                    </p><p>
                        在任何情况下，我们在保险期间内对被保险人一次或多次累计给付的恶性肿瘤住院医疗保险金、恶性肿瘤特殊门诊医疗保险金、恶性肿瘤住院前后门诊急诊医疗保险金总和以恶性肿瘤医疗保险金额为。当一次或累计给付的恶性肿瘤住院医疗保险金、恶性肿瘤特殊门诊医疗保险金、恶性肿瘤住院前后门诊急诊医疗保险金总和达到恶性肿瘤医疗保险金额时，我们给付恶性肿瘤医疗保险金的责任即时终止。
                    </p>
                    <p>
                        <span>保险金计算方法</span>
                    </p>
                    <p>
                        在本合同有效期内，各项保险金责任范畴内的医疗费用，按照如下公式计算应当给付的保险金：
                    </p>
                    <p>
                        (1)	若被保险人投保时已参加公费医疗或社会医疗保险，申请保险金时已从公费医疗或社会医疗保险获得医疗费用补偿的，医疗保险金=被保险人发生的合理且必要的医疗费用金额总和-被保险人从公费医疗、社会医疗保险取得的医疗费用补偿金额总和-免赔额；
                    </p>
                    <p>
                        (2)	若被保险人投保时已参加公费医疗或社会医疗保险，申请保险金时未从公费医疗或社会医疗保险获得医疗费用补偿的，医疗保险金=（被保险人发生的合理且必要的医疗费用金额总和-免赔额）*60%；
                    </p>
                    <p>
                        (3)	若被保险人投保时未参加公费医疗或社会医疗保险，医疗保险金=被保险人发生的合理且必要的医疗费用金额总和-免赔额。
                    </p>
                    <p>
                        上述“免赔额”由您与我们约定，并在保险单上载明。
                    </p>
                    <p>
                        若被保险人在疾病观察期后经医院初次确诊罹患恶性肿瘤且在医院接受住院治疗的，对于自确诊恶性肿瘤之日起所发生的符合本合同约定的全部医疗费用，我们在给付保险金时，不再扣除免赔额。
                    </p>
                    <p>
                        投保人不间断投保本合同的，若被保险人在以往保险期间内已确诊罹患恶性肿瘤且接受住院治疗的，对于本合同保险期间内发生的符合本合同约定的全部医疗费用，我们在给付保险金时，不再扣除免赔额。
                    </p>
                    <p>
                        <span>补偿原则</span>
                    </p>
                    <p>
                        若被保险人已从其他途径（包括社会医疗保险、公费医疗、工作单位、包括我们在内的任何商业保险机构等）取得补偿的，我们将按上述约定计算并给付保险金，且最高给付金额不超过被保险人实际发生的医疗费用扣除其所获补偿后的余额。
                    </p>
                    <p class="p-f-title">
                        2.6 <span>责任免除</span>
                    </p>
                    <p>因下列情形之一导致被保险人支出医疗费用的，我们不承担保险金给付责任：</p>
                    <p>(1)投保人对被保险人的故意杀害、故意伤害；</p>
                    <p>(2)被保险人故意犯罪或者抗拒依法采取的刑事强制措施；</p>
                    <p>
                        (3)被保险人自杀，但被保险人自杀时为无民事行为能力人的除外；
                    </p>
                    <p>(4)被保险人<b class="b-color">醉酒</b>，<b class="b-color">斗殴</b>，主动吸食或注射<b class="b-color">毒品</b>；
                    </p>
                    <p>
                        (5)被保险人<b class="b-color">酒后驾驶</b>、<b class="b-color">无合法有效驾驶证驾驶</b>，或驾驶<b class="b-color">无有效行驶证</b>的<b class="b-color">机动车</b>；
                    </p>
                    <p>(6)被保险人在首次投保或间断投保前所患<b class="b-color">既往症</b>；疾病观察期内出现的疾病、<b class="b-color">症状</b>或者<b class="b-color">体征</b>；疾病观察期内接受检查但在疾病观察期后确诊的疾病；</p>
                    <p>(7)被保险人在不符合本合同约定的医院就诊发生的医疗费用；</p>
                    <p>
                        (8)被保险人在首次投保或间断投保的合同生效之日起一百二十天内接受扁桃体、甲状腺、疝气、女性生殖系统疾病的检查与治疗；
                    </p>
                    <p>(9)被保险人患精神性疾病（依据世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD10）分类为精神和行为障碍的疾病）；</p>
                    <p>(10)被保险人因妊娠（含宫外孕）、流产、分娩（含剖宫产）导致的伤害；因怀孕（含宫外孕）、流产、分娩（含剖腹产）、避孕、绝育手术、治疗不孕不育症、人工受孕及由此导致的并发症；</p>
                    <p>
                        (11)被保险人因<b class="b-color">医疗事故</b>、药物过敏导致的伤害；
                    </p>
                    <p>
                        (12)被保险人未遵医嘱，私自使用药物，但按使用说明的规定使用<b class="b-color">非处方药</b>的除外；
                    </p>
                    <p>
                        (13)整形手术、美容或整容手术、变性手术及前述手术的并发症；
                    </p>
                    <p>(14)被保险人患遗传性疾病，先天性畸形、变形或染色体异常（依据世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD10）确定）；</p>
                    <p>(15)牙科疾病及相关治疗，视力矫正手术，但因意外所致的不受此限；</p>
                    <p>
                        (16)被保险人因预防、康复、保健性或非疾病治疗类项目发生的医疗费用；眼镜、义齿、义眼、义肢、助听器等康复性器具；包皮环切术、包皮剥离术、包皮气囊扩张术、性功能障碍治疗；
                    </p>
                    <p>(17)被保险人参加职业运动或可获得报酬的运动或竞技，在训练或比赛中受伤；被保险人参加<b class="b-color">潜水</b>、跳伞、<b class="b-color">攀岩</b>、驾驶滑翔机或滑翔伞、<b class="b-color">探险</b>、摔跤、<b class="b-color">武术比赛</b>、<b class="b-color">特技表演</b>、赛马、赛车等高风险活动；</p>
                    <p>(18)战争、军事冲突、暴乱或武装叛乱；</p>
                    <p>
                        (19)核爆炸、核辐射或核污染；
                    </p>
                    <p>
                        (20)被保险人<b class="b-color">感染艾滋病病毒或患艾滋病</b>；
                    </p>
                    <p>
                        (21)被保险人支出的医疗费用中依法已由第三者赔偿的部分。
                    </p>
                    <p>
                        (22)被保险人在指定医疗机构的高端病房（包括特需病房、国际部病房、外宾病房、VIP 病房、干部病房等）、高端门诊（包括特需门诊、国际/外宾门诊等）或指定医疗机构范围外的其他医疗机构的医疗费用。
                    </p>
                    <p>发生以上情形之一，导致被保险人身故的，本合同终止，我们退还保险单的现金价值。</p>

                    <p class="p-title p-f-title">3.【理赔服务条款】</p>
                    <p>
                        3.1 <span>受益人 </span>
                    </p>
                    <p>除另有指定外，本合同保险金的受益人为被保险人本人。</p>
                    <p class="p-f-title">
                        3.2 <span>保险事故通知</span>
                    </p>
                    <p>您、被保险人或受益人知道保险事故后应当在10日内通知我们。如果您、被保险人或受益人故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，我们对无法确定的部分，不承担给付保险金的责任，但我们通过其他途径已经及时知道或者应当及时知道保险事故发生或者虽未及时通知但不影响我们确定保险事故的性质、原因、损失程度的除外。
                    </p>
                    <p class="p-f-title">
                        3.3 <span>保险金申请</span>
                    </p>
                    <p>申请人须填写保险金给付申请书，并提供下列证明和资料：</p>
                    <p>(1)保险合同或电子保险单号；</p>
                    <p>(2)申请人的有效身份证件；</p>
                    <p>(3)支持索赔的证明、信息和证据，包含但不限于医院出具的病历资料、医学诊断书、药品明细处方、病理检查报告、血液检查报告、以其他科学方法作出的检验报告、手术记录及费用明细单据等。保险金申请人因特殊原因不能提供上述材料的，应提供其他合法有效的材料；</p>
                    <p>(4)医院出具的医疗费用原始凭证和医疗费用结算清单（被保险人享有社会医疗保险或公费医疗保障的，需包含按社会医疗保险或公费医疗有关规定取得医疗费用补偿的证明的原始凭证）；</p>
                    <p>(5)申请人所能提供的与确认保险事故的性质、原因、伤害程度等有关的其他证明和资料；</p>
                    <p>(6)若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。</p>
                    <p>保险金申请人未能提供有关材料，导致我们无法核实该申请的真实性的，我们对无法核实部分不承担给付保险金的责任。</p>
                    <p>以上各项保险金申请的证明和资料不完整的，我们将及时一次性通知申请人补充提供有关证明和资料。</p>
                    <p class="p-f-title">
                        3.4 <span>保险金给付</span>
                    </p>
                    <p>
                        我们在收到保险金给付申请书及合同约定的证明和资料后，将在5个工作日内作出核定；<b class="b-color">情形复杂</b>的，在30日内作出核定。对属于保险责任的，我们在与受益人达成给付保险金的协议后10日内，履行给付保险金义务。我们未及时履行前款规定义务的，除支付保险金外，应当赔偿受益人因此受到的损失。前述“损失”是指根据中国人民银行公布的同时期的人民币活期存款基准利率计算的利息损失。
                    </p>
                    <p>对不属于保险责任的，我们自作出核定之日起3日内向受益人发出拒绝给付保险金通知书并说明理由。</p>
                    <p>我们在收到保险金给付申请书及有关证明和资料之日起60日内，对给付保险金的数额不能确定的，根据已有证明和资料可以确定的数额先予支付；我们最终确定给付保险金的数额后，将支付相应的差额。
                    </p>
                    <p class="p-f-title">
                        3.5 <span>诉讼时效</span>
                    </p>
                    <p>受益人向我们请求给付保险金的诉讼时效期间为2年，自其知道或者应当知道保险事故发生之日起计算。</p>


                    <p class="p-title p-f-title">4.【释义】</p>
                    <p>
                        4.1 <span>周岁</span>
                    </p>
                    <p>
                        指按有效身份证件中记载的出生日期计算的年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。
                    </p>
                    <p class="p-f-title">
                        4.2 <span>公费医疗</span>
                    </p>
                    <p>指国家通过医疗卫生部门向享受人员提供的制度规定范围内的免费医疗预防，是国家为保障享受人员身体健康而设立的一种社会保障制度。</p>
                    <p class="p-f-title">
                        4.3 <span>社会医疗保险</span>
                    </p>
                    <p>指政府举办的基本医疗保障项目，包括城镇职工基本医疗保险、城镇居民基本医疗保险、新农合、医疗救助等。</p>
                    <p class="p-f-title">
                        4.4 <span>有效身份证件</span>
                    </p>
                    <p>指由政府主管部门规定的证明其身份的证件，如：居民身份证、按规定可使用的有效护照、军官证、警官证、士兵证、户口簿等证件。</p>
                    <p class="p-f-title">
                        4.5 <span>现金价值</span>
                    </p>
                    <p>指本合同保险单所具有的价值，通常体现为解除合同时，根据精算原理计算的，由我们退还的那部分金额。现金价值＝本合同的保险费×65％×（1－n/m），其中n为本合同已生效天数，m为本合同保险期间的天数。合同已生效的天数不足一天的不计。</p>
                    <p class="p-f-title">
                        4.6 <span>意外伤害</span>
                    </p>
                    <p>指遭受外来的、突发的、非本意的、非疾病的使身体受到伤害的客观事件。</p>
                    <p class="p-f-title">
                        4.7 <span>指定医疗机构</span>
                    </p>
                    <p>指中国境内（不含香港、澳门和台湾地区）经中华人民共和国卫生行政管理部门正式评定的二级以上（含二级）公立医院，该医院必须符合国家有关医院管理规则设置标准的医疗设备，且全天二十四小时有合格医师及护士驻院提供医疗及护理服务,且仅限于上述医院的普通部，不包括如下机构或医疗服务：</p>
                    <p>
                        1. 特需医疗、外宾医疗、干部病房、联合病房、国际医疗中心、VIP部、联合医院；
                    </p>
                    <p>
                        2. 诊所、康复中心、家庭病床、护理机构、疗养院，精神心理治疗中心以及无相应医护人员或设备的二级或二级以上的联合医院或联合病房；
                    </p>
                    <p>
                        3. 休养、戒酒、戒毒中心。
                    </p>
                    <p class="p-f-title">
                        4.8 <span>住院治疗</span>
                    </p>
                    <p>指被保险人因患疾病无法以门诊的方式提供安全医疗而入住医院之正式病房进行治疗，并正式办理入出院手续，但不包括住入急诊观察室、家庭病床、联合病房、康复医院（病房）、疗养院、挂床住院及其他不合理的住院。挂床住院是指被保险人住院过程中1日内未接受与入院诊断相关的检查和治疗，或1日内住院不满24小时（遵医嘱到外院接受临时诊疗的除外）。</p>
                    <p class="p-f-title">
                        4.9 <span>合理且必要</span>
                    </p>
                    <p>指被保险人发生的医疗费用应符合下列所有条件：</p>
                    <p>(1)治疗疾病所必需的项目；</p>
                    <p>(2)不超过安全、足量治疗原则的项目；</p>
                    <p>(3)由医师开具的处方药或医嘱；</p>
                    <p>(4)非试验性的、研究性的项目；</p>
                    <p>(5)与接受治疗当地普遍接受的医疗专业实践标准一致的项目。</p>
                    <p>对是否合理且必要由我们根据客观、审慎、合理的原则进行核定，若被保险人对核定结果有不同意见，可委托双方认可的权威医学机构或者权威医学专家进行审核鉴定。</p>
                    <p class="p-f-title">
                        4.10 <span>住院医疗费用</span>
                    </p>
                    <p>指被保险人住院期间发生的医疗费用，包括：</p>
                    <p>(1)床位费：指住院期间实际发生的、不高于双人病房的住院床位费（不包括单人病房、套房、家庭病床）。</p>
                    <p>(2)加床费：指未满18周岁的被保险人在住院治疗期间，我们根据合同约定给付其合法监护人（限一人）在医院留宿发生的加床费；或女性被保险人在住院治疗期间，我们根据合同约定给付其一周岁以下哺乳期婴儿在医院留宿发生的加床费。</p>
                    <p>(3)重症监护室床位费：指住院期间出于<b class="b-color">医学必要</b>被保险人需在重症监护室进行合理且必要的医疗而产生的床位费。重症监护室指配有中心监护台、心电监护仪及其他监护抢救设施，相对封闭管理，符合重症监护病房（ICU）、冠心病重症监护病房（CCU）标准的单人或多人监护病房。</p>
                    <p>(4)护理费：指住院期间根据医嘱所示的护理等级确定的护理费用。</p>
                    <p>(5)膳食费：指住院期间根据医生的医嘱，由作为医院内部专属部门的、为住院病人配餐的食堂配送的膳食费用。膳食费应包含在医疗账单内；根据各医疗机构的惯例，可以作为独立的款项，也可以合并在病房费等其他款项内。</p>
                    <p>(6)检查检验费：指住院期间实际发生的，以诊断疾病为目的，采取必要的医学手段进行检查及检验而发生的合理的医疗费用，包括X光费、心电图费、B超费、脑电图费、内窥镜费、肺功能仪费、分子生化检验费和血、尿、便常规检验费等。</p>
                    <p>(7)治疗费：指住院期间以治疗疾病为目的，提供必要的医学手段而发生的合理的治疗者的技术劳务费和医疗器械使用费，以及消耗品的费用，具体以就诊医院的费用项目划分为准。本项责任不包含如下费用：<b class="b-color">物理治疗、</b><b class="b-color">中医理疗及其他特殊疗法</b>费用。</p>
                    <p>(8)药品费：指住院期间实际发生的合理且必要的由医生开具的具有国家药品监督管理部门核发的药品批准文号或者进口药品注册证书、医药产品注册证书的国产或进口药品的费用。药品费中不包含中草药费用。</p>
                    <p>(9)医生费：指包括外科医生、麻醉师、内科医生、专科医生的费用。</p>
                    <p>(10)手术费：指当地卫生行政部门规定的手术项目的费用。包括手术费、麻醉费、手术监测费、手术材料费、术中用药费、手术设备费；若因器官移植而发生的手术费用，不包括器官本身的费用和获取器官过程中的费用。</p>
                    <p>(11)救护车使用费：指住院期间以抢救生命或治疗疾病为目的，根据医生建议，被保险人需医院转诊过程中的医院用车费用，且救护车的使用仅限于同一城市中的医疗运送。</p>
                    <p class="p-f-title">
                        4.11 <span>门诊</span>
                    </p>
                    <p>
                        指被保险人确因临床需要，正式办理挂号手续，并确实在医疗机构的普通门诊部（不含特需、国际/外宾部）接受治疗的行为过程，但不包括休养、疗养、身体检查和健康护理等非治疗性行为。
                    </p>
                    <p class="p-f-title">
                        4.12 <span>恶性肿瘤</span>
                    </p>
                    <p>指恶性细胞不受控制的进行性增长和扩散，浸润和破坏周围正常组织，可以经血管、淋巴管和体腔扩散转移到身体其他部位的疾病。经病理学检查结果明确诊断，临床诊断属于世界卫生组织《疾病和有关健康问题的国际统计分类》（ICD-10）的恶性肿瘤范畴，其中不包含：</p>
                    <p>(1)原位癌；</p>
                    <p>(2)相当于Binet分期方案A期程度的慢性淋巴细胞白血病；</p>
                    <p>(3)相当于Ann Arbor分期方案I期程度的何杰金氏病；</p>
                    <p>(4)皮肤癌（不包括恶性黑色素瘤及已发生转移的皮肤癌）；</p>
                    <p>(5)TNM分期为T<sub>1</sub>N<sub>0</sub>M<sub>0</sub>期或更轻分期的前列腺癌；</p>
                    <p>(6)感染艾滋病病毒或患艾滋病期间所患恶性肿瘤。</p>
                    <p>本合同所保障的恶性肿瘤须经专科医生明确诊断，专科医生应当同时满足以下四项资格条件：</p>
                    <p>(1)具有有效的中华人民共和国《医师资格证书》；</p>
                    <p>(2)具有有效的中华人民共和国《医师执业证书》，并按期到相关部门登记注册；</p>
                    <p>(3)具有有效的中华人民共和国主治医师或主治医师以上职称的《医师职称证书》；</p>
                    <p>(4)在二级或二级以上医院的相应科室从事临床工作三年以上。</p>
                    <p class="p-f-title">
                        4.13 <span>化学疗法</span>
                    </p>
                    <p>指针对于恶性肿瘤的化学治疗（即化疗）。化疗是使用医学界公认的化疗药物以杀死癌细胞、抑制癌细胞生长繁殖为目的而进行的治疗。本合同所指的化疗为被保险人根据医嘱，在医院进行的静脉注射化疗。</p>
                    <p class="p-f-title">
                        4.14 <span>放射疗法</span>
                    </p>
                    <p>指针对恶性肿瘤的放射治疗（即放疗）。放疗是使用各种不同能量的射线照射肿瘤组织，以抑制和杀灭癌细胞为目的而进行的治疗。本合同所指的放疗为被保险人根据医嘱，在医院的专门科室进行的放疗。</p>
                    <p class="p-f-title">
                        4.15 <span>肿瘤免疫疗法</span>
                    </p>
                    <p>指应用免疫学原理和方法，使用肿瘤免疫治疗药物提高肿瘤细胞的免疫原性和对效应细胞杀伤的敏感性，激发和增强机体抗肿瘤免疫应答，并应用免疫细胞和效应分子输注宿主体内，协同机体免疫系统杀伤肿瘤、抑制肿瘤生长。本合同所指的肿瘤免疫治疗药物需符合法律、法规要求并经过国家食品药品监督管理总局批准用于临床治疗。</p>
                    <p class="p-f-title">
                        4.16 <span>肿瘤内分泌疗法</span>
                    </p>
                    <p>指针对于恶性肿瘤的内分泌疗法，用药物抑制激素生成和激素反应，杀死癌细胞或抑制癌细胞的生长。本合同所指的内分泌治疗药物需符合法律、法规要求并经过国家食品药品监督管理总局批准用于临床治疗。</p>
                    <p class="p-f-title">
                        4.17 <span>肿瘤靶向疗法</span>
                    </p>
                    <p>指在细胞分子水平上，针对已经明确的致癌点来设计相应的靶向治疗药物，利用具有一定特异性的载体，将药物或其他杀伤肿瘤细胞的活性物质选择性地运送到肿瘤部位攻击癌细胞的疗法。本合同所指的靶向治疗的药物需具有国家药品监督管理部门核发的药品批准文号或者进口药品注册证书、医药产品注册证书。</p>
                    <p class="p-f-title">
                        4.18 <span>恶性肿瘤住院医疗费用</span>
                    </p>
                    <p>因恶性肿瘤引起的住院医疗费用，包括床位费、加床费、重症监护室床位费、护理费、膳食费、检查检验费、治疗费、药品费、医生费、手术费、救护车使用费。具体费用释义详见“4.10 住院医疗费用”。</p>
                    <p class="p-f-title">
                        4.19 <span>醉酒</span>
                    </p>
                    <p>指因饮酒而表现出动作不协调、意识紊乱、舌重口吃或其他不能清醒地控制自己行为的状态。醉酒的认定，如有司法机关、公安部门的有关法律文件、医院的诊断书等，则以上述法律文件、诊断书等为准。</p>
                    <p class="p-f-title">
                        4.20 <span>斗殴</span>
                    </p>
                    <p>指双方或多方通过拳脚、器械等武力以求制胜的行为。斗殴的认定，如有司法机关、公安部门的有关法律文件，则以上述法律文件为准。</p>
                    <p class="p-f-title">
                        4.21 <span>毒品</span>
                    </p>
                    <p>指中华人民共和国刑法规定的鸦片、海洛因、甲基苯丙胺（冰毒）、吗啡、大麻、可卡因以及国家规定管制的其他能够使人形成瘾癖的麻醉药品和精神药品，但不包括由医生开具并遵医嘱使用的用于治疗疾病但含有毒品成分的处方药品。</p>
                    <p class="p-f-title">
                        4.22 <span>酒后驾驶</span>
                    </p>
                    <p>指经检测或鉴定，发生事故时车辆驾驶人员每百毫升血液中的酒精含量达到或超过一定的标准，公安机关交通管理部门依据《道路交通安全法》的规定认定为饮酒后驾驶或醉酒后驾驶。</p>
                    <p class="p-f-title">
                        4.23 <span>无合法有效驾驶证驾驶</span>
                    </p>
                    <p>指下列情形之一：</p>
                    <p>(1)没有取得驾驶资格；</p>
                    <p>(2)驾驶与驾驶证准驾车型不相符合的车辆；</p>
                    <p>(3)持审验不合格的驾驶证驾驶；</p>
                    <p>(4)持学习驾驶证学习驾车时，无教练员随车指导，或不按指定时间、路线学习驾车。</p>
                    <p class="p-f-title">
                        4.24 <span>无有效行驶证</span>
                    </p>
                    <p>指下列情形之一：</p>
                    <p>(1)机动车被依法注销登记的；</p>
                    <p>(2)未依法按时进行或通过机动车安全技术检验。</p>
                    <p class="p-f-title">
                        4.25 <span>机动车</span>
                    </p>
                    <p>指以动力装置驱动或者牵引，上道路行驶的供人员乘用或者用于运送物品以及进行工程专项作业的轮式车辆。</p>
                    <p class="p-f-title">
                        4.26 <span>既往症</span>
                    </p>
                    <p>指在本合同生效前罹患的被保险人已知或应该知道的有关疾病。通常有以下情况：</p>
                    <p>(1)本合同生效前，医生已有明确诊断，长期治疗未间断；</p>
                    <p>(2)本合同生效前，医生已有明确诊断，治疗后症状未完全消失，有间断用药情况；</p>
                    <p>(3)本合同生效前，未经医生诊断和治疗，但症状或体征明显且持续存在，以普通人医学常识应当知晓。</p>
                    <p class="p-f-title">
                        4.27 <span>症状</span>
                    </p>
                    <p>指被保险人病后对机体生理功能异常的自身体验和感觉。</p>
                    <p class="p-f-title">
                        4.28 <span>体征</span>
                    </p>
                    <p>指被保险人的体表或内部结构发生可以察觉的改变。</p>
                    <p class="p-f-title">
                        4.29 <span>医疗事故</span>
                    </p>
                    <p>指医疗机构及其医务人员在医疗活动中，违反医疗卫生管理法律、行政法规、部门规章和诊疗护理规范、常规，过失造成患者人身损害的事故。</p>
                    <p class="p-f-title">
                        4.30 <span>非处方药</span>
                    </p>
                    <p>指在使用药品当时，由国务院药品监督管理部门公布的，不需要凭执业医师和执业助理医师处方，消费者可以自行判断、购买和使用的药品。</p>
                    <p class="p-f-title">
                        4.31 <span>潜水</span>
                    </p>
                    <p>指使用辅助呼吸器材在江、河、湖、海、水库、运河等水域进行的水下运动。</p>
                    <p class="p-f-title">
                        4.32 <span>攀岩</span>
                    </p>
                    <p>指攀登悬崖、楼宇外墙、人造悬崖、冰崖、冰山等运动。</p>
                    <p class="p-f-title">
                        4.33 <span>探险</span>
                    </p>
                    <p>指明知在某种特定的自然条件下有失去生命或使身体受到伤害的危险，而故意使自己置身于其中的行为，如：江河漂流、登山、徒步穿越沙漠或人迹罕至的原始森林等活动。</p>
                    <p class="p-f-title">
                        4.34 <span>武术比赛</span>
                    </p>
                    <p>指两人或两人以上对抗性柔道、空手道、跆拳道、散打、拳击等各种拳术及使用器械的对抗性比赛。</p>
                    <p class="p-f-title">
                        4.35 <span>特技表演</span>
                    </p>
                    <p>指进行马术、杂技、驯兽等表演。</p>
                    <p class="p-f-title">
                        4.36 <span>感染艾滋病病毒或患艾滋病</span>
                    </p>
                    <p>艾滋病病毒指人类免疫缺陷病毒，英文缩写为 HIV。 艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为 AIDS。在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。</p>
                    <p class="p-f-title">
                        4.37 <span>情形复杂</span>
                    </p>
                    <p>指保险事故的性质、原因、伤害或损失程度等在我们收到保险金给付申请书及合同约定的证明和资料后5个工作日内无法确定，需要进一步核实。</p>
                    <p class="p-f-title">
                        4.38 <span>医学必要</span>
                    </p>
                    <p>指被保险人接受治疗或服务、使用器械或服用药品符合以下条件：</p>
                    <p>(1)医师处方要求且对治疗被保险人疾病或伤害合适且必需；</p>
                    <p>(2)在范围、持续期、强度、护理上不超过为被保险人提供安全、恰当、合适的诊断或治疗所需的水平；</p>
                    <p>(3)与接受治疗当地普遍接受的医疗专业实践标准一致；</p>
                    <p>(4)非主要为了个人舒适或为了被保险人父母、家庭、医师或其他医疗提供方的方便；</p>
                    <p>(5)非病人学术教育或职业培训的一部分或与之相关；</p>
                    <p>(6)非试验性或研究性。</p>
                    <p class="p-f-title">
                        4.39 <span>物理治疗、中医理疗及其他特殊疗法</span>
                    </p>

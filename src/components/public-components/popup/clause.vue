            <br>（二）预防接种异常反应或偶合症医疗保险责任
            <br>在保险期间内，被保险人在经人民政府卫生主管部门指定的医疗卫生机构接种本保险合同约定范围内的疫苗后发生预防接种异常反应或偶合症，并在释义医院或接种疫苗的医疗卫生机构进行治疗，保险人就被保险人自该异常反应或偶合症诊断之日起180日内实际支出的按照当地社会医疗保险主管部门规定可报销的、必要的、合理的医疗费用，在扣除保险单载明的免赔额后按本保险合同约定的赔付比例计算给付预防接种异常反应或偶合症医疗保险金。 

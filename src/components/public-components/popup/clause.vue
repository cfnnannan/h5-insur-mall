            <br>（1）被保险人因同一保险事故造成两处或两处以上伤残时，应首先根据《评定标准》对各处伤残程度分别进行评定，如果几处伤残等级不同，以最重的伤残等级作为最终的评定结论并据此给付残疾保险金；如果两处或两处以上伤残等级相同，伤残等级在原评定基础上最多晋升一级，最高晋升至第一级。同一部位和性质的伤残，不应采用《评定标准》条文两条以上或者同一条文两次以上进行评定。
            <br>（2）被保险人如在此次保险事故发生之前已有残疾，保险人按合并后的残疾程度在《评定标准》中所对应伤残等级的给付比例扣除原有残疾程度在《评定标准》中所对应伤残等级的给付比例后，再乘以身故保险金额给付残疾保险金。

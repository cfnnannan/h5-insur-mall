            <br>（一）身故保险责任
            <br>在保险期间内，被保险人在经人民政府卫生主管部门指定的医疗卫生机构接种本保险合同约定范围内的疫苗后发生预防接种不良反应或偶合症，并自不良反应或偶合症诊断之日起180日内因该不良反应或偶合症为直接原因身故的，保险人按本保险合同约定的身故保险金额给付身故保险金，对该被保险人的保险责任终止。

            <br>在保险期间内，被保险人在经人民政府卫生主管部门指定的医疗卫生机构接种本保险合同约定范围内的疫苗后发生预防接种不良反应或偶合症后需要住院治疗的，保险人按被保险人的实际住院天数扣除保险单载明的免赔天数后，乘以保险单载明的住院伙食津贴日金额计算并给付住院伙食津贴保险金，最高累计给付天数为180天。累计给付天数达到180天时，保险人对被保险人的该项保险责任终止。
            <br>保险期间届满被保险人治疗仍未结束的，保险人继续承担给付住院伙食津贴保险金责任，最长延长至保险期满之日起30日止。
            <p class="title-item">责任免除</p>
            第六条  发生下列情形的，保险人不承担给付保险金责任：
            <br>（1）投保人、被保险人的故意或重大过失行为；
            <br>（2）被保险人或其家属不配合手术或不执行医嘱，擅自使用药物而造成的不良后果；
            <br>（3）被保险人或其家属不遵守医院规章制度、不配合治疗；
            <br>（4）使用过期、变质、质量不合格的疫苗或使用未经国家药品监督管理部门认可的疫苗，但被保险人在不知情的情况下使用前述疫苗的情形不在此限；
            <br>（5）疫苗实施差错事故；
            <br>（6）参加免疫接种前，被保险人已患有相关的严重慢性疾病、器官病变及体质过敏；
            <br>（7） 被保险人有疫苗说明书规定的接种禁忌，在接种前被保险人或者其监护人未如实提供被保险人的健康状况和接种禁忌等情况，接种后被保险人原有疾病急性复发或者病情加重；
            <br>（8） 因心理因素发生的个体或者群体的心因性反应；
            <br>（9）被保险人未按规定程序按时接受疫苗的预防接种；
            <br>（10）分娩前婴儿本身存在的严重疾病或缺陷；
            <br>（11）其他不属于保险责任范围内的费用。
            <p class="title-item">保险金额和保险费</p>

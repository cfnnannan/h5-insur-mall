            <br>被保险人身故前，保险人根据本合同针对其已给付妊娠手术意外全残保险金的，妊娠手术意外身故保险金应当扣除已给付金额。
            <br>2.妊娠手术意外全残保险责任
            <br>被保险人遭受该意外，并自该手术意外发生之日起180日内被评定为因该事故造成的《人身保险伤残评定标准与代码》（保监发【2014】6号国家金融行业标准编号JR/T0083－2013）（以下简称为《行业标准》）中所列伤残程度第一级之一者，保险人按本主险保险金额一次性给付全残保险金，对该被保险人的保险责任终止。
            <br>在给付第（二）项保险责任保险金之前，如该被保险人已领取过第（一）项保险责任保险金，保险人将不再给付第（二）项保险责任保险金。
            <br>（三）妊娠并发症身故保险责任
            <br>被保险人在保险人指定或保险人认可的医疗机构分娩或接受与分娩相关的医疗时，出现弥漫性血管内凝血、侵蚀性葡萄胎、胎盘早期脱落、子痫症、羊水栓塞相关并发症，并以如上并发症为直接且单独原因导致被保险人身故的，保险人根据本保险合同约定的妊娠并发症身故保险金额给付保险金，本保险合同效力终止。
            <br>二、子宫切除保险责任
            <br>在保险期间内，并在保险人与投保人约定孕周起，年龄在35周岁及以下的被保险人在保险人指定或保险人认可的医疗机构分娩或接受与分娩相关的医疗时，出现产科急性子宫出血导致必须对被保险人进行子宫切除手术，保险人根据本保险合同约定给付子宫切除保险金，对被保险人的该项保险责任终止。
            <br>在给付第一项保险责任保险金之前，如该被保险人已领取过第二项保险责任保险金，保险人在给付第一项保险责任保险金时应当扣除已给付的第二项保险金。
            <br>三、流产、引产保险责任
            <br>在保险期间内，并在保险人与投保人约定孕周起，被保险人因遭受意外伤害或胎儿非正常发育为直接原因导致流产、引产需进行手术治疗的，保险人根据本保险合同约定的该项保险金额给付保险金，对被保险人的该项保险责任终止。
            <br>四、新生儿先天畸形保险责任
            <br>在保险期间内，并在保险人与投保人约定孕周起，连带被保险人自出生之日起七日内（含出生日）经保险人指定或保险人认可的医疗机构确诊存有本保险合同列明的先天畸形，保险人按照保险合同约定的新生婴儿先天畸形保险金额给付保险金，对被保险人的该项保险责任终止。
            <br>五、连带被保险人身故保险责任
            <br>在保险人与投保人约定孕周起，被保险人在保险人指定或保险人认可的医疗机构分娩过程（不包括流产、引产）中因意外或疾病，并以其为直接且单独原因导致连带被保险人在出生后七日内（含出生日）身故的，保险人根据本保险合同约定的连带被保险人的身故保险金额给付身故保险金，保险人对该连带被保险人的保险责任终止。
            <br>在给付第五项保险责任保险金之前，如该被保险人已领取过第四项保险责任保险金，保险人将不再给付第五项保险责任保险金。
            <p class="title-item">责任免除</p>
            第六条 在保险期间内，因下列原因导致保险事故发生的，保险人不承担给付保险金责任：
            <br>（一）投保人与被保险人的故意行为；
            <br>（二）被保险人（不含连带被保险人）患有先天性疾病、精神类疾病、遗传性疾病、先天性畸形、变形或染色体异常；
            <br>（三）免疫系统缺陷或自身免疫性疾病；
            <br>（四）国家法定传染病以及地方病；
            <br>（五）被保险人或连带被保险人非分娩过程中猝死（包括不明原因的死亡）；
            <br>（六）被保险人未遵从医嘱，私自服用、涂用、注射药物，拒绝配合治疗或延误治疗；
            <br>（七）任何生物、化学、原子能武器，原子能或核能装置所造成的爆炸、灼伤、污染或辐射；
            <br>（八）连带被保险人在出生前已经诊断出患有先天性疾病或发育不全；
            <br>（九）被保险人异位妊娠（含宫外孕）、不孕不育症（含人工受孕、试管婴儿）治疗（如减胎术）或习惯性流产；
            <br>（十）投保时或在首次投保九十天内已经患有恶性肿瘤、职业病、先天性疾病、遗传性疾病、性传播疾病、糖尿病、高血压、子宫肌瘤等不适合生育的基础性疾病；
            <br>（十一）被保险人不符合投保年龄或约定孕周限制或未正式登记结婚；
            <br>（十二）在不符合本合同约定的医院分娩或医疗；
            <br>（十三）被保险人药物过敏、食物中毒或进行整容整形手术导致的伤害；
            <br>（十四）被保险人进行除妊娠手术以外的其他医疗事故导致的伤害；
            <br>（十五）被保险人代孕或未到法定结婚年龄；
            <br>（十六）被保险人为成骨不全症患者；
            <br>（十七）被保险人既往有不良孕产史，或妊娠并发症、合并症史。
            <br>第七条 被保险人在下列期间发生保险事故的，保险人也不承担给付保险金责任：
            <br>（一）战争、军事行动、暴动、恐怖活动或武装叛乱期间；
            <br>（二）被保险人醉酒或受酒精、毒品、管制药物的影响期间；
            <br>（三）被保险人从事违法、犯罪活动期间或被依法拘留、服刑期间；
            <br>（四）被保险人患有艾滋病（AIDS）或感染艾滋病病毒（HIV阳性）期间；
            <br>（五）被保险人乘坐经公安机关交通管理部门认定为醉酒后驾驶的机动车；
            <br>（六）被保险人进行除治疗性流产以外的人工流产，基中治疗性流产是指为了抢救孕妇生命或健康所做的流产；
            <br>（七）被保险人酒后驾驶、无合法有效驾驶证或驾驶无有效行驶证的机动车。
            <br>若由于本保险合同中责任免除的情形导致的被保险人或连带被保险人死亡，本合同对该被保险人或连带被保险人的责任终止，保险人将退还该被保险人对应的未满期净保费。
            <p class="title-item">保险金额和保险费</p>
            第八条 保险金额是保险人承担给付保险金责任的最高限额。本保险合同各分项保险责任的保险金额有投保人与保险人双方协商确定，并在保险单中载明。保险金额一经确定，中途不得变更。
            <br>连带被保险人为两人或两人以上的，每个连带被保险人的保险金额按实际存活的连带被保险人的人数均分。
            <br>本保险合同的保险费由保险人根据保险金额和被保险人的具体风险状况等因素确定，并在保险单上载明其金额。投保人应该按照合同约定向保险人交纳保险费。
            <p class="title-item">保险期间和等待期</p>
            第九条 本合同保险期间由保险人和投保人协商确定，以保险单载明的起讫时间为准，最长不超过一年。
            <br>除另有约定外，投保人为被保险人首次投保本保险或非连续投保本保险时，部分保险责任设定有固定的等待期，且保险人将在本合同中进行约定。
            <br>被保险人在等待期内发生保险事故，保险人均不承担支付保险金的责任。
            <p class="title-item">保险人义务</p>
            第十条 订立保险合同时，采用保险人提供的格式条款的，保险人向投保人提供的投保单应当附格式条款，保险人应当向投保人说明保险合同的内容。对保险合同中免除保险人责任的条款，保险人在订立合同时应当在投保单、保险单或者其他保险凭证上作出足以引起投保人注意的提示，并对该条款的内容以书面或者口头形式向投保人作出明确说明；未作提示或者明确说明的，该条款不产生效力。
            <br>第十一条 本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。
            <br>第十二条 保险人认为有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人或者受益人补充提供。
            <br>第十三条 保险人收到被保险人的给付保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内作出核定，但保险合同另有约定的除外。
            <br>保险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成给付保险金的协议后十日内，履行赔偿保险金义务。保险合同对给付保险金的期限有约定的，保险人应当按照约定履行给付保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝给付保险金通知书，并说明理由。
            <br>第十四条 保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其给付的数额不能确定的，应当根据已有证明和资料可以确定的数额先予以支付；保险人最终确定给付的数额后，应当支付相应的差额。
            <p class="title-item">投保人、被保险人或受益人义务</p>
            第十五条 除另有约定外，投保人应当在保险合同成立时交清保险费。投保人若未按约定支付保险费，则保险费交付前发生的保险事故，保险人不承担保险金给付责任。
            <br>第十六条 订立保险合同，保险人就保险标的或者被保险人的有关情况提出询问的，投保人应当如实告知。
            <br>投保人故意或者因重大过失未履行前款规定的义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本合同。
            <br>前款规定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。
            <br>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。
            <br>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，但应当退还保险费。
            <br>保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。
            <br>第十七条 投保人住所或联系方式变更时，应及时以书面形式通知保险人。投保人未通知的，保险人按本保险合同所载的最后住所或联系方式发送的有关通知，均视为已发送给投保人。
            <br>第十八条 投保人、被保险人或者保险金受益人知道保险事故发生后，应当及时通知保险人。故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。
            <br>上述约定，不包括因不可抗力而导致的迟延。
            <p class="title-item">保险金申请与给付</p>
            第十九条 保险金申请人向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法核实该申请的真实性的，保险人对无法核实部分不承担给付保险金的责任。
            <br>（一）被保险人保险金申请
            <br>（1）保险索赔申请书；
            <br>（2）保险单原件；
            <br>（3）受益人的户籍证明或身份证明；与被保险人的关系证明；
            <br>（4）二级以上（含二级）医院或相关主管部门出具的被保险人门诊妊娠体检病历（档案）、临床诊断报告、住院病历和医疗收据；
            <br>（5）被保险人的身份证明、户籍注销证明、结婚证；
            <br>（6）投保人、被保险人或者受益人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；
            <br>（7）保险人认可的权威机构出具的医疗事故报告
            <br>（8）若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。
            <br>（二）连带被保险人保险金申请
            <br>（1）保险索赔申请书；
            <br>（2）保险单原件；
            <br>（3）受益人的户籍证明或身份证明；与被保险人的关系证明；
            <br>（4）二级以上（含二级）医院或相关主管部门出具的被保险人门诊妊娠体检病历（档案）、临床诊断报告、住院病历和医疗收据；
            <br>（5）被保险人的户籍证明或身份证明、结婚证；
            <br>（6）投保人、被保险人或者受益人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；
            <br>（7）若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。
            <br>第二十条 保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。
            <p class="title-item">争议处理和法律适用</p>
            第二十一条 因履行本合同发生的争议，由当事人协商解决。协商不成的，提交保险单中载明的仲裁机构仲裁；保险单中未载明仲裁机构且争议发生后未达成仲裁协议的，依法向中华人民共和国法院起诉。
            <br>第二十二条 与本合同有关的以及履行本合同产生的一切争议，适用中华人民共和国法律（不含港、澳、台地区法律）。
            <p class="title-item">其他事项</p>
            第二十三条 在本保险合同成立后，投保人可以书面形式通知保险人解除合同，但保险人已根据本保险合同约定给付保险金的除外。
            <br>投保人解除本保险合同时，应提供下列证明文件和资料：
            <br>（1）保险合同解除申请书；
            <br>（2）保险单原件；
            <br>（3）保险费交付凭证；
            <br>（4）投保人身份证明；
            <br>（5）投保人所能提供的其他与保险合同有关的文件和资料。
            <br>投保人要求解除本保险合同，自保险人接到保险合同解除申请书之时起，本保险合同的效力终止。保险人收到上述证明文件和资料之日起三十日内退还保险单的未满期净保费。
            <p class="title-item">释义</p>
            1、周岁：以法定身份证明文件中记载的出生日期为基础计算的实足年龄，自出生之日起为零周岁，每经过一年增加一岁，不足一年的不计。
            <br>2、保险人：指与投保人签订本保险合同的易安财产保险有限公司。
            <br>3、弥漫性血管内凝血：是指妊娠所并发的致命性疾病，因凝血功能障碍导致全身性出血不止及器官损伤，需经专科医生确诊，并提供同时具有下列条件中的至少三项的检验报告： 
            <br>（a） 血小板计数小于100×109 L 或者呈进行性下降； 
            <br>（b） 血浆纤维蛋白原含量小于1.5g/L 或者﹥4g/L 或者呈进行性下降； 
            <br>（c） 3P 试验阳性或者血浆FDP>20mg/L； 
            <br>（d） 凝血酶原时间﹥15 秒或者超过对照组3 秒以上。 
            <br>4、侵蚀性葡萄胎： 是指异常增生的绒毛组织浸润性生长浸入子宫肌层或者转移至其他器官或者组织的葡萄胎，并已经进行化疗或者手术治疗的。
            <br>5、胎盘早期分离：指怀孕满二十周后，胎盘于胎儿产出前先行脱离，以致胎儿窘迫或者母体休克。胎盘早期脱离需达第二或者第三级的脱离而施以紧急剖腹产手术，且需经专科医生确诊。
            <br>6、子痫症：又称“重度妊娠高血压综合症”，指血压持续高于160mmHg/110mmHg、蛋白尿≧5g/24h 或者尿常规中蛋白（++）-（++++）和（或者）伴水肿，有头痛等自觉症状，并且有抽搐或者昏迷。需经专科医生确诊，并提供同时具有下列条件中的至少两项的医学证明： 
            <br>（a） 血肌酐升高（﹥1.6mg%）； 
            <br>（b） 少尿（24 小时总尿量少于500 毫升）； 
            <br>（c） 出现神经系统的异常或者视力异常； 
            <br>（d） 肺水肿； 
            <br>（e） 黄疸进行性加重； 
            <br>（f） 胎儿宫内死亡； 
            <br>（g） 血小板减少，凝血症； 

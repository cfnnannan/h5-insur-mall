            <br>（一）预防接种一般反应医疗保险责任
            <br>在保险期间内，被保险人在经人民政府卫生主管部门指定的医疗卫生机构接种本保险合同约定范围内的疫苗后发生预防接种一般反应，并在符合本条款释义的医院（以下简称“释义医院”）或接种疫苗的医疗卫生机构进行治疗，保险人就被保险人自该一般反应诊断之日起180日内实际支出的按照当地社会医疗保险主管部门规定可报销的、必要的、合理的医疗费用，在扣除保险单载明的免赔额后按本保险合同约定的赔付比例计算给付预防接种一般反应医疗保险金。
            <br>被保险人无论一次或多次发生预防接种一般反应，保险人均按上述规定给付预防接种一般反应医疗保险金，但累计给付金额以该被保险人的预防接种一般反应医疗保险金额为限，累计给付金额达到预防接种一般反应医疗保险金额时，对被保险人的该项保险责任终止。
